// Build countries array from flags.json
type FlagEntry = { code: string; unicode: string; name: string; emoji: string };
const flags = flagsData as Record<string, FlagEntry>;

export const countries: CountryData[] = Object.entries(flags)
  .filter(([code]) => code.length === 2) // Skip codes like "CYP", "US-HI"
  .map(([code, flag]) => {
    const details = countryDetails[code];
    return {
      name: flag.name,
      code: flag.code,
      emoji: flag.emoji,
      landmark: details?.landmark ?? "Scenic landscape",
      icon: details?.icon ?? "national emblem",
      region: details?.region ?? "Other",
    };
  })
  .sort((a, b) => a.name.localeCompare(b.name));
